    PrePay è una piattaforma di lending crowdfunding creata da investitori per gli investitori. È la prima soluzione in Italia costruita interamente secondo la logica INVESTOR FIRST. Vuol dire che abbiamo progettato ogni parte del nostro ecosistema pensando agli investitori come te.
    Non sarai mai più “uno dei tanti”

    Se hai già esperienza nel crowdfunding o nel lending sai che a volte, quando investi in un progetto attraverso un sito o una piattaforma, hai la sgradevole sensazione di essere “uno dei tanti” e di non avere alcun controllo sulle somme che hai impegnato, né informazioni sufficienti sull’andamento dei tuoi investimenti. Possiamo garantirti che in PrePay è tutto diverso, perché da noi l’investitore è al centro di tutto.
    In PrePay sei sempre al centro di tutto

    Dal sito web, alle schede informative dei progetti, fino al wallet o ai webinar di aggiornamento mensili, ogni strumento PrePay è stato progettato per consentirti di effettuare i tuoi investimenti in modo facile e sicuro. E avere sempre sotto controllo il tuo denaro. Per questo la chiamiamo logica investor first.
    La Formula PrePay

    Un’altra innovazione che ci rende unici è il pagamento anticipato degli interessi. Se partecipi a un progetto che aderisce alla “Formula PrePay” potrai riscuotere l’intero interesse del tuo investimento entro quindici giorni dalla chiusura della raccolta. In questo modo avrai subito a disposizione una somma aggiuntiva, (il rendimento del capitale che hai investito), che potrai decidere se investire in un altro progetto, oppure trasferire semplicemente sul tuo conto.

